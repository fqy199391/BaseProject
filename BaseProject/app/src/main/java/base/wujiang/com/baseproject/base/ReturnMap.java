package base.wujiang.com.baseproject.base;

import java.io.Serializable;

/**  
 * 概要说明 : 公共返回实体参数  <br>
 */
public class ReturnMap implements Serializable
{

    /**  
     * serialVersionUID:(用一句话描述这个变量表示什么).  
     */
    private static final long serialVersionUID = 6175198353779980762L;

    /**  
     * code:正确错误标志1、-1 
     */
    private int code;

    /**  
     * msg:消息  
     */
    private String msg;

    /**  
     * data:实体数据  
     */
    private Object data;

    public ReturnMap()
    {
        super();
        this.code = -1;
        this.msg = "获取数据异常";
    }

    public int getCode()
    {
        return code;
    }

    public void setCode(int code)
    {
        this.code = code;
    }

    public String getMsg()
    {
        return msg;
    }

    public void setMsg(String msg)
    {
        this.msg = msg;
    }

    public Object getData()
    {
        return data;
    }

    public void setData(Object data)
    {
        this.data = data;
    }

}
